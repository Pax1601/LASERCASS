% Return TAS given a differential pressure (difference between pitot and static pressures)
% and altitude
function tas = dp2tas(dp, alt)

  [rho0, p0, T0, A0, mu0] = ISA_h(0);
  [rho, p, T, A, mu] = ISA_h(alt);
  Tratio = T / T0;
  pratio = p / p0;
  dratio = pratio / Tratio;
  rho = rho0 * dratio;
  [tas, err] = dp2speed(dp, p, rho);
  if (err==1)
    error('The speed found exceeds 661.48 knots.');
  end  
end
