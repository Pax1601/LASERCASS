%**************************************************************************
% Callbacks
%**************************************************************************
    function DownloadFromSite(hobj, e)
        % This too should change 'status' to -1 (it stops NeoCASS workflow)
        
        % By default try opening system browser
        SysBrowserStat = web(fullURL, '-browser');
        
        switch SysBrowserStat
            
            case 0
                status = -1;
                
            case {1, 2}
                % If the system browser is unavailable don't stop workflow:
                % try using the Matlab one, otherwise run NeoCASS
                MatBrowserMsg = {'System browser not found', 'System browser not working'};
                fprintf(' ** (%s): switching to Matlab browser... **\n', MatBrowserMsg{SysBrowserStat});
                
                % Try using Matlab browser instead
                MatBrowserStat = web(fullURL);
                
                switch MatBrowserStat
                    
                    case 0
                        status = -1;
                        
                    case {1, 2}
                        
                        % Exit changing status
                        MatBrowserMsg = {'Matlab browser not found', 'Matlab browser not working'};
                        fprintf(' ** (%s) **\n', MatBrowserMsg{MatBrowserStat});
                        status = 0;
                        
                end
                
        end
        CloseFig();
        
    end

    function Continue2NeoCASS(hobj, e)
        % Leave status to 0
        CloseFig();
    end

    function QuitWindow(hobj, e)
        % Quit
        % This should change 'status' to -1
        status = -1;
        CloseFig();
    end

    function CloseFig()
        %
        delete(hfig);
        
    end

    function CloseWindow(hobj, e)
        % Do nothing
    end

